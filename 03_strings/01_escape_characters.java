public class SecuenciasDeEscape {

    public static void main(String[] args) {
        System.out.println("---- Uso de Secuencias de Escape ----");
        
        // Uso de \n para un salto de línea
        System.out.println("Línea uno.\nLínea dos.");

        // Uso de \t para una tabulación
        System.out.println("Columna 1\tColumna 2");

        // Uso de \" para comillas dobles dentro de una cadena
        System.out.println("El libro se llama \"El Quijote\".");

        // Uso de \\ para una barra invertida
        System.out.println("La ruta del archivo es C:\\Users\\Documentos");
    }
}