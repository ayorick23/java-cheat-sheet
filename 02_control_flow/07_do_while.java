public class DoWhile {

    public static void main(String[] args) {

        // Bucle do-while
        int j = 5;
        System.out.println("\nBucle do-while (se ejecuta al menos una vez):");
        
        do {
            System.out.println("Valor: " + j);
            j++;
        } while (j < 5);
    }
}